	private boolean toggle = true;
	private List<Point> measurementPoints = new ArrayList<Point>();
	private List<Measurement> measurements = new ArrayList<Measurement>();
	private ImageViewer imageViewer;
	private Cursor measurementCursor;
	private MouseListener setPointListener = new MouseAdapter() {

		@Override
		public void mouseDown(MouseEvent e) {
			Point p = new Point(e.x, e.y);
			measurementPoints.add(p);
			if (imageViewer.getLastPoint() != null) {
				Measurement m = ImagesUIFactory.eINSTANCE.createMeasurement();
				m.setFrom(imageViewer.getLastPoint());
				m.setTo(p);
				measurements.add(m);
			}
			imageViewer.setLastPoint(p);
			imageViewer.setMeasurements(measurements);
		}

		@Override
		public void mouseUp(MouseEvent e) {
			imageViewer.redraw();
		}

	};

	private MouseMoveListener drawPairListener = new MouseMoveListener() {

		@Override
		public void mouseMove(MouseEvent e) {
			if (measurementPoints.isEmpty())
				return;
			imageViewer.redraw();
		}

	};

	@Override
	public void addHandlerListener(IHandlerListener handlerListener) {

	}

