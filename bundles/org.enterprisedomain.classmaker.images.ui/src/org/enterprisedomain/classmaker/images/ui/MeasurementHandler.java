	public void dispose() {
	}

	@Override
	public Object execute(ExecutionEvent event) throws ExecutionException {
		IEditorPart e = HandlerUtil.getActiveEditor(event);
		Shell shell = HandlerUtil.getActiveShell(event);
		if (!e.getEditorSite().getId().equals(ImageViewer.ID)) {
			Cursor firstCursor = shell.getDisplay().getSystemCursor(SWT.CURSOR_ARROW);
			shell.setCursor(firstCursor);
			return null;
		}
		imageViewer = (ImageViewer) HandlerUtil.getActiveEditor(event);
		if (toggle) {
			toggle = !toggle;
			changeCursor(shell);
			imageViewer.addMouseListener(setPointListener);
			imageViewer.setMeasurementPoints(measurementPoints);
			imageViewer.setMeasurements(measurements);
			imageViewer.addMouseMoveListener(drawPairListener);
			imageViewer.redraw();
		} else {
			toggle = !toggle;
			imageViewer.removeMouseMoveListener(drawPairListener);
			imageViewer.removeMouseListener(setPointListener);
			imageViewer.setLastPoint(null);
			imageViewer.redraw();
			Cursor firstCursor = shell.getDisplay().getSystemCursor(SWT.CURSOR_ARROW);
			shell.setCursor(firstCursor);
		}

		return null;
	}

